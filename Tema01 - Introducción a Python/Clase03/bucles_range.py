print(range(8))
print('--Iteración con range--')
# El valor de i inicia en 0
# El maximo valor de i es 7 (8-1)
for i in range(8):
    print(i)
print('--Iteración de bloque con range--')
print(range(3,8))
# El valor de i inicia en 3
# El maximo valor de i es 7 (8-1)
# Se repite 5 veces (8-3)
for i in range(3,8):
    print(i)

print('-----------------')
for i in range(2,6):
    print(i)